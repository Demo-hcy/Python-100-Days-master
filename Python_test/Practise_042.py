"""类型转换"""
print('int("23.5"):\t',int (23.5))  # 浮点数 -->整数
print('int("23.001"):\t',int (23.001))  # 浮点数 -->整数
print('int("23"):\t',int ("23"))  # 字符串 -->整数
print('float("23"):\t',float (23))  # 整数 -->浮点数
print('float("23"):\t',float ("23"))  # 字符串 -->浮点数
print('float("23.5"):\t',float ("23.5"))  # 字符串 -->浮点数
print('str("23"):\t',str (23))  # 整数 -->字符串
print('str("23.5"):\t',str (23.5))  # 浮点数 -->字符串
